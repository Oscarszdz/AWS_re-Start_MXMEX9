# Defining variables
a = 4
b = 2

print(f'variable a =', a)
print(f'variable b =', b)

# Adding
print(f'Adding a + b', ',', str(a), '+', str(b), '=', a + b)

# Substracting
print(f'Substracting a - b', ',', str(a), '-', str(b), '=', a - b)

# Multiplying
print(f'Multiplying a * b', ',', str(a), '*', str(b), '=', a * b)

# Dividing
print(f'Dividing a / b', ',', str(a), '/', str(b), '=', a / b)

# Editing a Python file
print(f'Python has three numeric types: int, float and complex')

# Creating a variable
myValue = 1

print(f'Printing myValue {myValue}')
print(f'Printing type of myValue {type(myValue)}')

# Combining numbers and text
print(str(myValue) + " is of the type " + str(type(myValue)))
print(f'{myValue} is of the type {type(myValue)}')

# Introducing the float data type
my_value = 3.14
print(f'Printing my_value {my_value}')
print(f'Printing type of my_value {type(my_value)}')

# Introducing the complex data type
my_complex_value = 5j
print(f'Printing my_complex_value {my_complex_value}')
print(f'Printing type of my_complex_value {type(my_complex_value)}')

# Introducing the bool data type
my_bool_value = True
print(f'Printing my_bool_value is {my_bool_value}')
print(f'Printing type of my_bool_value {type(my_bool_value)}')