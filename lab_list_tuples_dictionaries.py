# Defining a list
my_fruit_list = ["apple", "banana", "cherry"]
print(my_fruit_list)
print(f'Type of my_fruit_list {type(my_fruit_list)}')

# Accessing list by position
print(my_fruit_list[0])
print(my_fruit_list[1])
print(my_fruit_list[2])

# Changing the values in a list
my_fruit_list[2] = 'orange'
print(f'Print updated list: {my_fruit_list}')

# Introducing the typle data type
my_final_answer_tuple = ("apple", "banana", "pineapple")
print(f'Printing my Tuple: {my_final_answer_tuple}')
print(f'Type of my_final_answer_tupe: {type(my_final_answer_tuple)}')

# Accesing a tuple by position
print(my_final_answer_tuple[0])
print(my_final_answer_tuple[1])
print(my_final_answer_tuple[2])

# Introducing the dictionary data type
my_favorite_fruit_dictionary = {
    "Akua": "apple",
    "Saanvi": "banana",
    "Paulo": "pineapple"
}

print(f'Printing my dictionary: {my_favorite_fruit_dictionary}')
print(f'Type of my_favorite_fruit_dictionary is {type(my_favorite_fruit_dictionary)}')

# Accesing a dictionary by name
print(my_favorite_fruit_dictionary["Akua"])
print(my_favorite_fruit_dictionary["Saanvi"])
print(my_favorite_fruit_dictionary["Paulo"])